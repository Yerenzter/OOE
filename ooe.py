def isOdd(number):
    if number % 2 != 0:
        return str(number) + " is an odd number"
    else:
        return str(number) + " is not an odd number"

def isEven(number):
    if number % 2 == 0:
        return str(number) + " is an even number"
    else:
        return str(number) + " is not an even number"

def OOE(number):
    if number % 2 == 0:
        return str(number) + " is an even number"
    else:
        return str(number) + " is an odd number"
